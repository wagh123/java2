public class Basics {
    public static void main(String[] args) {
        System.out.println(5&6);//AND bit_wise Operator
        System.out.println(5|6);//OR
        System.out.println(5^6);//XOR
        System.out.println(~0);//1's complement
        System.out.println(5<<2);//left shift operator
        System.out.println(6>>1);//right shift
    }
}
